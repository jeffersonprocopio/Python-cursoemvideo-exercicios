n1 = int(input('Um valor: '))
n2 = int(input('Outro valor: '))
s = n1+n2
m = n1*n2
a = n1-n2
d = n1/n2
di = n1//n2
p = n1**n2
rd = n1%n2
print('A soma é {},a multiplicação é {} e a adição é {}'.format(s, m, a))
print('A divisão é {:.3f}, a divisão inteira é {}, a potênciação é {:.2f} e o resto da divisão é {}'.format(d, di, p, rd))

